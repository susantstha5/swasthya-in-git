                                                </li>
                                                <li>AST &amp; ALT - They are liver enzymes. Very high levels of AST and ALT are usually associated with acute
                                                    hepatitis while moderately increased levels may be seen in chronic hepatitis. ALT is usually higher than
                                                    AST in acute liver damage and in auto immune disorders while AST is higher than ALT in alcoholic hepatitis, cirrhosis, cancer</li>
                                                <li>Alkaline Phosphatase (ALP) - is an enzyme which is increased often to four times normal in obstruction of the bile ducts.
